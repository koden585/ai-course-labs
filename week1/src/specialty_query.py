# -*- coding: utf-8 -*
"""
Адаптированный запрос для специальности
Лабораторная работа №1

Специальность: "Фундаментальная информатика и информационные технологии"
Тема диплома: "Информационная система распознавания технических данных с графических материалов"
"""

import os
from dotenv import load_dotenv
from agent_core import YandexGPTClient

# Загрузка переменных окружения
load_dotenv()

def get_specialty_prompt() -> str:
    """
    Возвращает промпт, адаптированный под специальность студента.

    ЗАДАНИЕ: Замените этот промпт на свой, релевантный вашей теме диплома.
    """

    prompt = """
    Я студент технической специальности "Фундаментальная информатика и информационные технологии", работаю над дипломной темой:
    "Информационная система распознавания технических данных с графических материалов"
    
    Прошу предоставить информацию по следующим вопросам:
    1. Какие современные методы и алгоритмы используются для распознавания технических данных с изображений (включая OCR и Computer Vision)?
    2. Какие подходы применяются для обработки и анализа графических материалов (чертежей, схем, графиков) в информационных системах?
    3. Какие технологии и инструменты (библиотеки, фреймворки, модели) рекомендуется использовать для разработки такой системы?
    Требования к ответу:
    • Ответ должен быть структурирован
    • Используй технические термины
    • Приведи конкретные примеры
    • Объём: 300-500 слов
    """

    return prompt


def main():
    """Выполнение адаптированного запроса."""

    print("=" * 80)
    print("АДАПТИРОВАННЫЙ ЗАПРОС ПО СПЕЦИАЛЬНОСТИ")
    print("=" * 80)

    # Инициализация клиента
    iam_token = os.getenv("YANDEX_IAM_TOKEN")
    folder_id = os.getenv("YANDEX_FOLDER_ID")

    client = YandexGPTClient(iam_token, folder_id)

    # Получение персонализированного промпта
    prompt = get_specialty_prompt()

    print("\nЗАПРОС:")
    print("-" * 80)
    print(prompt)
    print("-" * 80)

    # Выполнение запроса
    print("\nОТВЕТ МОДЕЛИ:")
    print("-" * 80)

    response = client.generate(prompt, temperature=0.5)
    print(response["text"])

    print("-" * 80)
    print(f"\nТокены: вход={response['tokens_input']}, выход={response['tokens_output']}")

    # Сохранение результата
    with open("../docs/specialty_response.txt", "w", encoding="utf-8") as f:
        f.write("ЗАПРОС:\n")
        f.write(prompt)
        f.write("\n\nОТВЕТ:\n")
        f.write(response["text"])

    print("\n✅ Результат сохранён в docs/specialty_response.txt")


if __name__ == "__main__":
    main()
